public class Mencao {

    public static void main(String[] args) {
        String mencoes[] = new String [4];
        
        for(int i = 0; i < 4; i++){
            mencoes[0] = "MB";
            mencoes[1] = "B";
            mencoes[2] = "R";
            mencoes[3] = "I";
            
            System.out.println("As mensções são: " + mencoes[i]);
        }
    }
}
